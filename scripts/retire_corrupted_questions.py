ALL_RETIRE_QIDS = sorted(set(CORRUPT_QIDS + INCOMPLETE_QIDS + OPTION_LEAK_QIDS))


def main():
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--dry-run", action="store_true",
                   help="Print what would change; do not write")
    args = p.parse_args()

    init_db()
    db.connect(reuse_if_open=True)

    rows = list(Question.select().where(Question.id.in_(ALL_RETIRE_QIDS)))
    not_found = set(ALL_RETIRE_QIDS) - {r.id for r in rows}
    already_retired = [r for r in rows if r.status == "retired"]
    to_retire = [r for r in rows if r.status != "retired"]

    print(f"Plan:")
    print(f"  total in retire list:       {len(ALL_RETIRE_QIDS)}")
    print(f"  found in DB:                {len(rows)}")
    print(f"  not found (skipped):        {len(not_found)}")
    print(f"  already retired (no-op):    {len(already_retired)}")
    print(f"  will be marked retired:     {len(to_retire)}")
    if not_found:
        print(f"  missing IDs: {sorted(not_found)}")

    if args.dry_run:
        print()
        print("Dry-run; no changes written. Re-run without --dry-run to apply.")
        return

    if not to_retire:
        print()
        print("Nothing to do; every flagged question is already retired.")
        return

    print()
    print(f"Writing status='retired' to {len(to_retire)} rows...")
    with db.atomic():
        n = (Question
             .update(status="retired")
             .where(Question.id.in_([r.id for r in to_retire]))
             .execute())
    logger.info("retired %d corrupted questions", n)
    print(f"Done. Retired {n} questions.")


if __name__ == "__main__":
    main()

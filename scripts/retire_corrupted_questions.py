"""
One-shot script: retire the 73 questions the audit flagged as corrupted.

Marks `status='retired'` on each so that:
  - `services.question_bank.select_questions_composed` and
    `select_drill_smart` skip them (both filter on `status='live'`)
  - the diagnostic + practice flows never serve them
  - the user-visible "wrong answer + mismatched explanation" reports stop

Idempotent: re-running on already-retired rows is a no-op. The same QID
list lives inside migration `006_retire_corrupted_2026_04` so cloned-fresh
machines reach the same state without running this script.

PR 7's `regenerate_retired_answers.py` later attempts to re-derive the
correct answer + explanation via a two-pass LLM consensus and un-retires
on agreement.

Usage:
    venv/bin/python scripts/retire_corrupted_questions.py        # apply
    venv/bin/python scripts/retire_corrupted_questions.py --dry-run
"""
